# 10.Can hash values be arrays? Can you have an array of hashes? (give examples)

# Yes hash values can be arrays. Examples:

hash = {numbers: ['1', '2', '3']}
hash = {towns: ['Berwick', 'Conway', 'Portland']}

# Yes you can have an array of hashes. Examples:

arr = [{a:1}, {b:2}, {c:3}]
arr = [{town: 'berwick'}, {town: 'Conway'}, {town: 'Portland'}]

