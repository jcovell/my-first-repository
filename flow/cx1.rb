# Example 1
if x == 3
    puts "x is 3"
end

# Ruby allow you to append the if condition at the very end.  The
#above could be rewritten like this:

puts "x is 3" if x == 3 
end

# can also us a Ruby reserved word, unless.  It acts as the 
# opposite of if:

puts "x is NOT 3" unless x == 3 
end

  
  
  
  